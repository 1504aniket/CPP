#include <iostream>
using namespace std;
int main(){
    // int takes 4 byte memory 
    int a=5  ;
    // char takes 1 byte 
    char ch='a';
    // bool takes 1 byte 
    bool b=true;
    // float takes 4 byte 
    float c=1.2345;
    // double takes 8 byte 
    double d=2.3456;
    cout<<a<<endl;
    cout<<ch<<endl;
    cout<<b<<endl;
    cout<<c<<endl;
    cout<<d;

}